    private void jcbEmployeeIdPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_jcbEmployeeIdPopupMenuWillBecomeInvisible
         String empid = jcbEmployeeId.getSelectedItem().toString();
        String qry = "select * from employees where empid = ?";
        try{
            Connection conn = DBConnection.getConnection();
            PreparedStatement ps = conn.prepareStatement(qry);
            ps.setString(1,empid);
            ResultSet rs = ps.executeQuery();
            if(rs.next())
            {
                txtEmployeeName.setText(rs.getString(2));
                txtjob.setText(rs.getString(3));
                txtSalary.setText(rs.getString(4));
            }
        }
        catch(SQLException s){
            s.printStackTrace();
        }
    }//GEN-LAST:event_jcbEmployeeIdPopupMenuWillBecomeInvisible

