    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       if(validateInputs()==false)
            {
         JOptionPane.showMessageDialog(null,"please input all values");
         return;
        }
         try{
           String eno = jcbEmployeeId.getSelectedItem().toString();
            String ename = txtEmployeeName.getText();
            String job = txtjob.getText();
            double sal = Double.parseDouble(txtSalary.getText());
            
            EmpPojo e=new EmpPojo();
            
            e.setEmpid(eno);
            e.setEmpname(ename);
            e.setJob(job);
            e.setSal(sal);
            
            boolean result = EmpDao.deleteEmp(eno);
     
            if(result){
               JOptionPane.showMessageDialog(null,"Record Deleted");
               clearText();
               loadDetails();
            }
            else
                JOptionPane.showMessageDialog(null,"Record not found!");
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input digits only!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Problem in DB!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

