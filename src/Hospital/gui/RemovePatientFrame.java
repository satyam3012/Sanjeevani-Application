    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      if(validateInput()==false)
      {
          JOptionPane.showMessageDialog(null,"please select an Id");
          return;
      }
      try
      {
      String patientid = jcbPatientId.getSelectedItem().toString();
      
      boolean result = PatientDao.deletePatient(patientid);
      if(result)
      {
          JOptionPane.showMessageDialog(null,"Record Deleted Successfully!");
          loadPatientId();
      }
      else
          JOptionPane.showMessageDialog(null,"Record Not Found!!");
      } 
      catch(SQLException s)
      {
         JOptionPane.showMessageDialog(null,"ERROR in Database","ERROR",JOptionPane.ERROR_MESSAGE);
         s.printStackTrace();
      }
    }//GEN-LAST:event_btnDeleteActionPerformed

