    public void loadPatientId()
    {
     try
     {
      pid = PatientDao.addRegisteredPatient();
      if(pid.size()==0)
        {
          JOptionPane.showMessageDialog(null,"No Registered Patient are present!!");
          btnDelete.setEnabled(false);
          return;
        }
      btnDelete.setEnabled(true);
      Set key = pid.keySet();
      Iterator<String> it = key.iterator();
      while(it.hasNext())
      {
        jcbPatientId.addItem(it.next());  
      }
     }
     catch(SQLException s)
     {
      JOptionPane.showMessageDialog(null,"No registered recepionist present!","Error!",JOptionPane.ERROR_MESSAGE);   
     }
    }
    
    private boolean validateInput()
    {
     p = jcbPatientId.getSelectedItem().toString();
     if(p.isEmpty())
         return false;
     return true;
    }
    
