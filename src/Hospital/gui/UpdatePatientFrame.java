    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(validateInput()==false)
            {
         JOptionPane.showMessageDialog(null,"please input all values");
         return;
        }
         try{
            String opd = txtOPD.getText();
            String id = jcbPatientId.getSelectedItem().toString();
            String fname = txtFirstName.getText();
            String sname = txtSecondName.getText();
            int age = Integer.parseInt(txtAge.getText());
            String gender = (String)jcbGender.getSelectedItem().toString();
            String mstatus = (String)jcbStatus.getSelectedItem().toString();
            String address = txtAddress.getText();
            //String date =  txtDate.getText();
           String ward = jcbWardNo.getSelectedItem().toString();
           String bed = jcbBedNo.getSelectedItem().toString();
           
            String docid =  pid.get(jcbPatientId.getSelectedItem().toString());
            String city =  txtCity.getText();
            String mno = txtPhoneNo.getText(); 
            java.sql.Date date = new java.sql.Date(d.getTime());
            
            PatientPojo p=new PatientPojo();
            
            p.setP_id(id);
            p.setF_name(fname);
            p.setS_name(sname);
            p.setAge(age);
            p.setOpd(opd);
            p.setGender(gender);
            p.setM_status(mstatus);
            p.setDate(date);
            p.setAddress(address);
            p.setCity(city);
            p.setMno(mno);
            p.setDoctor_id(docid);
            p.setWard(ward);
            p.setBed(bed);
            
            boolean result = PatientDao.updatePatient(p);
            
            if(result)
            {
               JOptionPane.showMessageDialog(null,"Record update");
               clearText();
               
            }
            else
                JOptionPane.showMessageDialog(null,"Record not found!");
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input digits only!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Problem in DB!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }                           
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jcbPatientIdPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_jcbPatientIdPopupMenuWillBecomeInvisible
         String pd = jcbPatientId.getSelectedItem().toString();
        String qry = "select * from patient where p_id = ?";
        try{
            Connection conn = DBConnection.getConnection();
            PreparedStatement ps = conn.prepareStatement(qry);
            ps.setString(1,pd);
            ResultSet rs = ps.executeQuery();
            if(rs.next())
            {
                txtOPD.setText(rs.getString(5));
                txtFirstName.setText(rs.getString(2));
                txtSecondName.setText(rs.getString(3));
                txtAge.setText(rs.getString(4));
                jcbStatus.setSelectedItem(rs.getString(7));
                txtAddress.setText(rs.getString(9));
                txtCity.setText(rs.getString(10));
                jcbWardNo.setSelectedItem(rs.getString(13));
                jcbBedNo.setSelectedItem(rs.getString(14));
                jcbGender.setSelectedItem(rs.getString(6));
                txtDate.setText(rs.getString(8));
                txtPhoneNo.setText(rs.getString(11));
            }
        }
        catch(SQLException s){
            s.printStackTrace();
        }
    }//GEN-LAST:event_jcbPatientIdPopupMenuWillBecomeInvisible

