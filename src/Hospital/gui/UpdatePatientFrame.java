    private void loadPatientId(){
        
        try{
             pid=PatientDao.addRegisteredPatient();
             if(pid.size()==0)
        {
          JOptionPane.showMessageDialog(null,"No Registered Patient are present1!!");
          btnUpdate.setEnabled(false);
          return;
        }
      btnUpdate.setEnabled(true);
      Set key = pid.keySet();
      Iterator<String> it = key.iterator();
      while(it.hasNext())
      {
        jcbPatientId.addItem(it.next());  
      }
     }
     catch(SQLException s)
     {
      JOptionPane.showMessageDialog(null,"No registered recepionist present2!","Error!",JOptionPane.ERROR_MESSAGE);   
     }   
    }
    
