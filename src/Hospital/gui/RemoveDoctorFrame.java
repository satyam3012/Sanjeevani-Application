    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(validateInputs()==false)
            {
         JOptionPane.showMessageDialog(null,"please select one id");
         return;
        }
         try{
           String docid = jcbDoctorId.getSelectedItem().toString();
           String userid = doctor.get(jcbDoctorId.getSelectedItem().toString());
           
            boolean result = DoctorsDao.deleteDoctor(docid);
     
            if(result){
               JOptionPane.showMessageDialog(null,"Record Deleted successfully!!");
               loadDoctorDetails();
               
               try{
                   UserPojo2 e = new UserPojo2();
                   e.setUserId(userid);
                   boolean res = DoctorsDao.deleteAllDoctors(e);
                   if(res){
                     JOptionPane.showMessageDialog(null,"Record Deleted successfully from Database!!");  
                   }
                   else
                    JOptionPane.showMessageDialog(null,"Record not found!");
                  }
               catch(SQLException ex){
                    JOptionPane.showMessageDialog(null,"Problem in DB1!","Error",JOptionPane.ERROR_MESSAGE);
                   ex.printStackTrace();
                  }
                }
            else
                JOptionPane.showMessageDialog(null,"Record not found!");
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input digits only!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Problem in DB2!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

