     public void loadDoctorDetails(){
       try{
           doctor = DoctorsDao.getRegisteredDoctorId();
           
           if(doctor.size()==0)
           {
               JOptionPane.showMessageDialog(null,"1-No registered Doctors are present!");
               btnDelete.setEnabled(false);
               return;
           }  
           btnDelete.setEnabled(true);
           Set keys = doctor.keySet();
           Iterator<String> it = keys.iterator();
           jcbDoctorId.removeAllItems();
           while(it.hasNext())
           {
             jcbDoctorId.addItem(it.next());
           }         
       } 
       catch(SQLException sq)
       {
          JOptionPane.showMessageDialog(null,"2-No registered Doctors present!","Error!",JOptionPane.ERROR_MESSAGE);
       }      
    } 
    
     private boolean validateInputs()
    {
        docId=jcbDoctorId.getSelectedItem().toString();
       if(docId.isEmpty())
            return false;
        return true; 
    }
     
