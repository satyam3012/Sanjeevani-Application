    public void loadDetails(){
         try{
           hs = EmpDao.getRegisteredRecepionist();
           if(hs.size()==0)
           {
               JOptionPane.showMessageDialog(null,"No unregistered recepionist present!");
               btnUpdate.setEnabled(false);
               return;
           }
           
           btnUpdate.setEnabled(true);
           Set keys = hs.keySet();
           Iterator<String> it = keys.iterator();
           jcbEmployeeId.removeAllItems();
           while(it.hasNext())
           {
             jcbEmployeeId.addItem(it.next());
           }         
       } 
       catch(SQLException sq)
       {
          JOptionPane.showMessageDialog(null,"No extra registered recepionist present!","Error!",JOptionPane.ERROR_MESSAGE);
       }    
    }
    
    
