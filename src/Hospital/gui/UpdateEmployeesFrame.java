    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(validateInput()==false)
            {
         JOptionPane.showMessageDialog(null,"please input all values");
         return;
        }
         try{
            
            String eno = jcbEmployeeId.getSelectedItem().toString();
            String ename = txtEmployeeName.getText();
            double sal = Double.parseDouble(txtSalary.getText());
            
            EmpPojo e=new EmpPojo();
            e.setEmpid(eno);
            e.setEmpname(ename);
            e.setSal(sal);
            
            boolean result = EmpDao.updateEmp(e);
            
            if(result)
            {
               JOptionPane.showMessageDialog(null,"Record update");
               clearText();
               
            }
            else
                JOptionPane.showMessageDialog(null,"Record not found!");
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input digits only!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Problem in DB!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

